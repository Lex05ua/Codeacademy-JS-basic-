//declare Kelvin 
const kelvin = 293; 
 
//declare Celsius 
const celsius = kelvin - 273; 
 
//declare Fahrenheit 
let fahrenheit = celsius * (9/5) + 32; 
 
/* 
Use the .floor() method to round down the Fahrenheit temperature. 
 */ 
Math.floor(fahrenheit); 
 
console.log(`The temperature is ${fahrenheit} degrees Fahrenheit.`)
